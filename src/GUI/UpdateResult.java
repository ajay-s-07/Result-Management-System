    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String rollNo = jTextField1.getText();
        int Physics = Integer.parseInt(jTextField2.getText());
        int Maths = Integer.parseInt(jTextField3.getText());
        int Chemistry = Integer.parseInt(jTextField4.getText());
        int Electrical = Integer.parseInt(jTextField5.getText());
        int Biology = Integer.parseInt(jTextField6.getText());
        int Total = Physics + Maths + Chemistry + Electrical + Biology;
        
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE result SET physics="+Physics+",maths="+Maths+",chemistry="+Chemistry+",electrical="+Electrical+",biology="+Biology+",total="+Total+" where rollNo='"+rollNo+"'");
            JOptionPane.showMessageDialog(null,"Updated Successfully");
            setVisible(false);
            UpdateResult frame = new UpdateResult();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        }
        catch(Exception e){
            JOptionPane.showConfirmDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

