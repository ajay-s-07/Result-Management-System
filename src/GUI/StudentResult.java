    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollNo = jTextField1.getText();
        try{
            Connection con = ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollNo='"+rollNo+"' and result.rollNo='"+rollNo+"'");
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Invalid Roll No");
            }
            else{
                nameTF.setText(rs.getString(1));
                genderTF.setText(rs.getString(3));
                fatherTF.setText(rs.getString(4));
                courseTF.setText(rs.getString(5));
                branchTF.setText(rs.getString(6));
                
                jTextField7.setText(rs.getString(8));
                jTextField8.setText(rs.getString(9));
                jTextField9.setText(rs.getString(10));
                jTextField10.setText(rs.getString(11));
                jTextField11.setText(rs.getString(12));
                jTextField12.setText(rs.getString(13));
                
                nameTF.setEditable(false);
                genderTF.setEditable(false);
                fatherTF.setEditable(false);
                courseTF.setEditable(false);
                branchTF.setEditable(false);
                
                jTextField7.setEditable(false);
                jTextField8.setEditable(false);
                jTextField9.setEditable(false);
                jTextField10.setEditable(false);
                jTextField11.setEditable(false);
                jTextField12.setEditable(false);
                
                int phy = Integer.parseInt(rs.getString(8));
                int math = Integer.parseInt(rs.getString(9));
                int chem = Integer.parseInt(rs.getString(10));
                int elec = Integer.parseInt(rs.getString(11));
                int bio = Integer.parseInt(rs.getString(12));
                
                if(phy>=30 && math>=30 && chem>=30 && elec>=30 && bio>=30){
                    jTextField13.setText("PASS");
                }
                else{
                    jTextField13.setText("FAIL");
                }
                jTextField13.setEditable(false);
                
           }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

